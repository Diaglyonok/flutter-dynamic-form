import '../model/dynamic_form_models.dart';

class PhoneField extends Field {
  final String initialPrefix;

  PhoneField({
    required String fieldId,
    bool required = false,
    bool readOnly = false,
    required String label,
    String? labelId,
    bool? maskText,
    int? maxLength,
    String? inputType,
    List<Option>? options,
    String? confirmField,
    bool? strict,
    String? fieldName,
    int? versionId,
    CompositeValue? value,
    List<DependsOnValue>? dependsOn,
    List<Field>? children,
    bool? isCapitalized,
    String? validationExpression,
    String? validationErrorMessage,
    CompareDate? compareDate,
    int? parentId,
    required this.initialPrefix,
  }) : super(
          fieldName: fieldName,
          versionId: versionId,
          fieldId: fieldId,
          required: required,
          readOnly: readOnly,
          label: label,
          labelId: labelId,
          maskText: maskText,
          maxLength: maxLength,
          inputType: inputType,
          options: options,
          isCapitalized: isCapitalized,
          fieldType: FieldTypes.Phone,
          strict: strict,
          confirmField: confirmField,
          dependsOn: dependsOn,
          value: value,
          parentId: parentId,
          children: children,
          compareDate: compareDate,
          validationExpression: validationExpression,
          validationErrorMessage: validationErrorMessage,
        );
}
